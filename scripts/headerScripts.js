window.onload = function () {
    let burger_button_open = document.getElementById('burger_button_open')
    let burger_button_close = document.getElementById('burger_button_close')
    let burgermenu = document.getElementById('burgermenu')

    burger_button_open.onclick = function () {
        burgermenu.style.display = 'block'
    }

    burger_button_close.onclick = function () {
        burgermenu.style.display = 'none'
    }
}